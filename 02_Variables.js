/** VARIABLES:
 *  La variable NO ES un valor, ALMACENA un valor
 * cuando usamos una variable, esta devuelve el valor que fue almacenado en ella
 * 
 * - VAR: funcionan de manera GLOBAL. objetos, numeros, strings, arrays
 * - LET: funciona solo DENTRO DEL BLOQUE de código donde fueron declaradas
 * - CONST: constantes, NO cambian. SI O SI SE INICIALIZAN. se escriben con MAYSCULAS y guiones bajos los nombres.
 * 
 * 
 * 
 * 
 * las {} también se usan para delimitar un bloque de código (además de objetos)
 * 
 * 
 * TEMPLATE STRINGS: puden almacenar variables dentro de ellas.
 * se usan asi: por ejemplo, tengo una variable: var nombre = "Sole";
 * que quiero incluir en otra:
 * var saludo = `Hola soy ${nombre}`; o sea la estructura es:
 * abrir las comillas invertidas, y dentro se une con ${nombreVariable}.
 * admite espacios entre lineas!
 * ej: 
 * var saludo = `Hola soy ${nombre}
 * 
 * cómo estas?`;
 */